<p><span style='font-family:Arial;background:#D9D9D9'>I. General Teaching
Objectives:</span><span style='font-family:Arial'> <o:p></o:p></span></p>

<p><span style='font-family:Arial'>&nbsp;<br>
The Southern Connecticut Chinese School @ SCSU is a non-profit organization
registered in the state of Connecticut. Its mission is to teach Chinese language,
traditional Chinese arts, and to elevate the awareness of Chinese culture in
the community. The school sets its goal to enhance students’ Chinese language
skills as a second language. <o:p></o:p></span></p>

<p><span style='font-family:Arial'>The School admits students, conducts all
educational programs and activities without regard to race, color, religion,
sex, national origin, sexual orientation, age, marital status, ancestry or
disabilities.<o:p></o:p></span></p>

<p><span style='font-family:Arial'>Since the teaching strategies for the
students with Chinese language background (referred students from Chinese
heritage families hereafter) and without Chinese language background (referred
as students from non-Chinese heritage hereafter) are different, we design two
tracks for those two types in their low level class. Eventually the students
from both tracks should be combined together in the middle and high level.<o:p></o:p></span></p>

<p><span style='font-family:Arial'>For the students from Chinese heritage
families, the school designs a curriculum that would allow students to interact
their 2-hour weekly school work with their virtual use and practice of the
language at home under the guidance of parents. Besides language learning, the
school also endeavors to help students become acquainted with Chinese culture.<o:p></o:p></span></p>

<p><span style='font-family:Arial'>For the students from non-Chinese heritage
families, the School offers a different track of classes, to allow these
students to learn the basic skills of listening, speaking, reading and writing
in Chinese. <o:p></o:p></span></p>

<p><span style='font-family:Arial'>Our purpose is to help students learn
Chinese as a practical communication skill, and develop a life-long
appreciation for the Chinese language and culture; therefore, this curriculum
sets the benchmarks for the school teaching practice.<o:p></o:p></span></p>

